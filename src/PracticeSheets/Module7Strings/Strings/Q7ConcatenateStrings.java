package PracticeSheets.Module7Strings.Strings;

public class Q7ConcatenateStrings {
    //Write a program that takes two strings: "Hello" and "World",
    // and prints their concatenation: "HelloWorld".
    public static void main(String[] args) {
        String str1 = "Hello";
        String str2 = "World";

        String result = str1 + str2;

        System.out.println("Concatenated String: " + result);
    }
}

